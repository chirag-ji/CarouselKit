package com.github.chiragji.carouselkit.enums;

import android.widget.FrameLayout;

/**
 * Type of content to be rendered in the carousel pager
 *
 * @author Chirag [apps.chiragji@outlook.com]
 * @version 1
 * @since 1.0.0
 */
public enum ContentType {
    /**
     * Denotes that the {@link com.github.chiragji.carouselkit.CarouselItem} is a IMAGE
     */
    IMAGE,
    /**
     * Denotes that the {@link com.github.chiragji.carouselkit.CarouselItem} is a VIDEO
     */
    VIDEO,
    /**
     * Denotes that the {@link com.github.chiragji.carouselkit.CarouselItem} is a TEXT
     */
    TEXT
}
